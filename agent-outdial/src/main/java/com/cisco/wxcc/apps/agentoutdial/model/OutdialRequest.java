package com.cisco.wxcc.apps.agentoutdial.model;

public class OutdialRequest {

	private String login;
	
	private String ani;
	
	private String dnis;

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getAni() {
		return ani;
	}

	public void setAni(String ani) {
		this.ani = ani;
	}

	public String getDnis() {
		return dnis;
	}

	public void setDnis(String dnis) {
		this.dnis = dnis;
	}
}
